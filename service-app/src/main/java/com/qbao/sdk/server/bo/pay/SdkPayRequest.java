package com.qbao.sdk.server.bo.pay;

/**
 * @author mazhongmin
 * 支付获取用户信息请求对象
 */
public class SdkPayRequest {

	/** 交易类型**/
	private String transType;
	/** 应用编码**/
	private String appCode;
	/** 业务流水**/
	private String orderNo;
	/** 交易人id**/
	private long userId;
	/** 计费点编码**/
	private String billingCode;
	/** 应用渠道**/
	private int appSource;
	/** 交易简介**/
	private String transIntro;
	/** 异步通知**/
	private String payCallbackUrl;
	/** 签文**/
	private String signCode;
	/** 签名方式**/
	private String signType;
	/** 请求ip**/
	private String ip;
	public String getTransType() {
		return transType;
	}
	public void setTransType(String transType) {
		this.transType = transType;
	}
	public String getAppCode() {
		return appCode;
	}
	public void setAppCode(String appCode) {
		this.appCode = appCode;
	}
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public int getAppSource() {
		return appSource;
	}
	public void setAppSource(int appSource) {
		this.appSource = appSource;
	}
	public String getTransIntro() {
		return transIntro;
	}
	public void setTransIntro(String transIntro) {
		this.transIntro = transIntro;
	}
	public String getSignCode() {
		return signCode;
	}
	public void setSignCode(String signCode) {
		this.signCode = signCode;
	}
	public String getSignType() {
		return signType;
	}
	public void setSignType(String signType) {
		this.signType = signType;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getBillingCode() {
		return billingCode;
	}
	public void setBillingCode(String billingCode) {
		this.billingCode = billingCode;
	}
	public String getPayCallbackUrl() {
		return payCallbackUrl;
	}
	public void setPayCallbackUrl(String payCallbackUrl) {
		this.payCallbackUrl = payCallbackUrl;
	}
	
}
